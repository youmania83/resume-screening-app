// Middleware to resolve tenant context for guest candidates
async function resolveTenantContext(req: any, res: any, next: any) {
  // If recruiter/owner is logged in, authMiddleware has already set tenant context
  if (req.user && req.user.tenantId) {
    return next();
  }

  try {
    let tenantId: string | null = null;

    // 1. Resolve by assessment token
    const token = req.params?.token || req.body?.token || req.query?.token;
    if (token && typeof token === "string" && token.length > 20) {
      const result = await queryGlobal("SELECT tenant_id FROM candidates WHERE assessment_token = $1 LIMIT 1;", [token]);
      if (result.rowCount && result.rowCount > 0) {
        tenantId = result.rows[0].tenant_id;
      }
    }

    // 2. Resolve by candidateId
    const candidateId = req.body?.candidateId || req.query?.candidateId;
    if (!tenantId && candidateId && typeof candidateId === "string") {
      const result = await queryGlobal("SELECT tenant_id FROM candidates WHERE id = $1 LIMIT 1;", [candidateId]);
      if (result.rowCount && result.rowCount > 0) {
        tenantId = result.rows[0].tenant_id;
      }
    }

    // 3. Resolve by jobId
    const jobId = req.body?.jobId || req.params?.jobId || req.query?.jobId;
    if (!tenantId && jobId && typeof jobId === "string") {
      const result = await queryGlobal("SELECT tenant_id FROM jobs WHERE id = $1 LIMIT 1;", [jobId]);
      if (result.rowCount && result.rowCount > 0) {
        tenantId = result.rows[0].tenant_id;
      }
    }

    if (tenantId) {
      tenantStorage.run({ tenantId, userId: "guest", role: "candidate" }, () => {
        next();
      });
    } else {
      next();
    }
  } catch (err) {
    next(err);
  }
}

router.use(resolveTenantContext);

/**
 * POST /api/assessment/generate
 * Generates an assessment for a job title/description if it doesn't already exist.
 */
router.post("/generate", async (req: any, res: any) => {
  try {
    const { jobTitle, jobDescription, jobId } = req.body as {
      jobTitle: string;
      jobDescription: string;
      jobId?: string;
    };

    if (!jobTitle || !jobDescription) {
      return res.status(400).json({ error: "jobTitle and jobDescription are required" });
    }

    const id = jobId || `job-${Date.now()}-${Math.random().toString(36).substr(2, 5)}`;
    
    // Ensure the job exists in the jobs table
    const checkJob = await queryTenant(`SELECT id FROM jobs WHERE id = $1 AND tenant_id = :tenant_id;`, [id]);
    if (!checkJob.rowCount || checkJob.rowCount === 0) {
      await queryTenant(
        `INSERT INTO jobs (id, title, description, tenant_id) VALUES ($1, $2, $3, :tenant_id);`,
        [id, jobTitle, jobDescription]
      );
    }

    const assessmentId = await ensureJobAssessment(id, jobTitle, jobDescription);
    res.json({ success: true, jobId: id, assessmentId });
  } catch (err: any) {
    console.error("Assessment generation route failed:", err);
    res.status(500).json({ error: err.message || "Failed to generate assessment" });
  }
});

/**
 * POST /api/assessment/send
 * Sends assessment invite to a shortlisted candidate.
 */
router.post("/send", async (req: any, res: any) => {
  try {
    const { candidateId } = req.body as { candidateId: string };
    if (!candidateId) {
      return res.status(400).json({ error: "candidateId is required" });
    }

    // Fetch candidate
    const candidateRes = await queryTenant(
      `SELECT * FROM candidates WHERE id = $1 AND tenant_id = :tenant_id LIMIT 1;`,
      [candidateId]
    );

    if (!candidateRes.rowCount || candidateRes.rowCount === 0) {
      return res.status(404).json({ error: "Candidate not found" });
    }

    const candidate = candidateRes.rows[0];

    // Ensure job exists and matches role (backwards compatibility)
    let jobId = candidate.job_id;
    if (!jobId) {
      // Find or create job based on role
      const jobRes = await queryTenant(`SELECT id FROM jobs WHERE title = $1 AND tenant_id = :tenant_id LIMIT 1;`, [candidate.role]);
      if (jobRes.rowCount && jobRes.rowCount > 0) {
        jobId = jobRes.rows[0].id;
      } else {
        jobId = `job-${Date.now()}-${Math.random().toString(36).substr(2, 5)}`;
        await queryTenant(
          `INSERT INTO jobs (id, title, description, tenant_id) VALUES ($1, $2, $3, :tenant_id);`,
          [jobId, candidate.role, `Autogenerated job description for ${candidate.role}`]
        );
      }
      // Update candidate with job_id
      await queryTenant(`UPDATE candidates SET job_id = $1 WHERE id = $2 AND tenant_id = :tenant_id;`, [jobId, candidateId]);
    }

    // Ensure job assessment exists
    const jobDesc = `Autogenerated job description for ${candidate.role}`;
    await ensureJobAssessment(jobId, candidate.role, jobDesc);

    // Generate token
    const token = crypto.randomBytes(24).toString("hex");
    const expiry = new Date();
    expiry.setDate(expiry.getDate() + 7); // 7 days expiry

    await queryTenant(
      `UPDATE candidates 
       SET assessment_token = $1, assessment_token_expiry = $2, assessment_status = $3
       WHERE id = $4 AND tenant_id = :tenant_id;`,
      [token, expiry, "pending", candidateId]
    );

    // Log activity
    await queryTenant(
      `INSERT INTO candidate_activity_logs (candidate_id, event_type, message, tenant_id)
       VALUES ($1, $2, $3, :tenant_id);`,
      [candidateId, "assessment_invited", `Assessment invitation email sent. Link: /assessment/${token}`]
    );

    // Send the email
    await sendAssessmentInviteEmail({
      candidateName: candidate.name,
      candidateEmail: candidate.email,
      jobTitle: candidate.role,
      token,
      expiryDate: expiry,
    });

    res.json({ success: true, token, expiryDate: expiry });
  } catch (err: any) {
    console.error("Assessment send invite failed:", err);
    res.status(500).json({ error: err.message || "Failed to send assessment invitation" });
  }
});

/**
 * GET /api/assessment/:token
 * Candidate portal entry endpoint. Loads test and locks attempts.
 */
router.get("/:token", async (req: any, res: any) => {
  try {
    const { token } = req.params;
    const { sessionId } = req.query as { sessionId: string };

    if (!sessionId) {
      return res.status(400).json({ error: "sessionId query parameter is required to initialize session" });
    }

    // Fetch candidate by token
    const candidateRes = await queryGlobal(
      `SELECT c.*, j.title as job_title, j.description as job_description
       FROM candidates c
       LEFT JOIN jobs j ON c.job_id = j.id
       WHERE c.assessment_token = $1 LIMIT 1;`,
      [token]
    );

    if (!candidateRes.rowCount || candidateRes.rowCount === 0) {
      return res.status(404).json({ error: "Invalid or expired assessment link" });
    }

    const candidate = candidateRes.rows[0];

    // Check token expiry
    if (new Date(candidate.assessment_token_expiry) < new Date()) {
      return res.status(400).json({ error: "This assessment link has expired (7 days deadline passed)" });
    }

    // Find or generate assessment for the job on demand
    let assessmentId = "";
    const assessmentRes = await queryGlobal(
      `SELECT id FROM assessments WHERE job_id = $1 LIMIT 1;`,
      [candidate.job_id]
    );
    if (assessmentRes.rowCount && assessmentRes.rowCount > 0) {
      assessmentId = assessmentRes.rows[0].id;
    } else {
      // Auto-generate assessment on the fly!
      assessmentId = await ensureJobAssessment(candidate.job_id, candidate.role, candidate.job_description || `Job opening for ${candidate.role}`);
    }

    // Check if there is an existing attempt
    const attemptRes = await queryGlobal(
      `SELECT * FROM assessment_attempts WHERE candidate_id = $1 AND assessment_id = $2 LIMIT 1;`,
      [candidate.id, assessmentId]
    );

    let attempt;

    // Fetch questions (omit the correct answers for the candidate!)
    const questionsRes = await queryGlobal(
      `SELECT id, question_text, options, difficulty, topic 
       FROM assessment_questions 
       WHERE assessment_id = $1 
       ORDER BY id ASC;`,
      [assessmentId]
    );
    const questions = questionsRes.rows.map(q => ({
      id: q.id,
      questionText: q.question_text,
      options: typeof q.options === "string" ? JSON.parse(q.options) : q.options,
      difficulty: q.difficulty,
      topic: q.topic
    }));

    const TOTAL_TEST_TIME_SEC = 10 * 60; // 10 minutes

    if (!attemptRes.rowCount || attemptRes.rowCount === 0) {
      // Use the session ID passed by the frontend to prevent race conditions during double-fetch
      const sessionDbId = sessionId || crypto.randomUUID();
      // Capture client info for audit
      const browserFingerprint = req.headers['user-agent'] || null;
      const ipAddress = req.ip || req.headers['x-forwarded-for'] || null;
      // Create a new session record (attempt_id will be linked after attempt creation)
      await queryGlobal(
        `INSERT INTO assessment_sessions (id, candidate_id, assessment_id, attempt_id, status, browser_fingerprint, ip_address)
         VALUES ($1, $2, $3, NULL, $4, $5, $6);`,
        [sessionDbId, candidate.id, assessmentId, 'active', browserFingerprint, ipAddress]
      );
      // Create a brand new attempt linked to this session
      const attemptId = `attempt-${Date.now()}-${Math.random().toString(36).substr(2, 5)}`;
      await queryGlobal(
        `INSERT INTO assessment_attempts (id, candidate_id, assessment_id, status, session_id)
         VALUES ($1, $2, $3, $4, $5);`,
        [attemptId, candidate.id, assessmentId, "started", sessionDbId]
      );
      // Update the session with the attempt reference
      await queryGlobal(
        `UPDATE assessment_sessions SET attempt_id = $1 WHERE id = $2;`,
        [attemptId, sessionDbId]
      );

      // Log activity
      await queryGlobal(
        `INSERT INTO candidate_activity_logs (candidate_id, event_type, message)
         VALUES ($1, $2, $3);`,
        [candidate.id, "assessment_started", "Candidate started the assessment test."]
      );

      return res.json({
        success: true,
        candidateName: candidate.name,
        candidateEmail: candidate.email,
        jobTitle: candidate.role,
        remainingSeconds: TOTAL_TEST_TIME_SEC,
        questions,
        sessionId: sessionDbId,
        currentAnswers: {},
        currentQuestionIndex: 0,
      });
    } else {
      attempt = attemptRes.rows[0];

      // If attempt is already completed, deny access
      if (attempt.status === "completed") {
        return res.status(403).json({ error: "Assessment already completed and submitted." });
      }

      // Rejoining session checks:
      // Check session ID
      if (attempt.session_id !== sessionId) {
        // Retrieve the session to check its last heartbeat
        const sessionRes = await queryGlobal(
          `SELECT last_heartbeat FROM assessment_sessions WHERE id = $1 LIMIT 1;`,
          [attempt.session_id]
        );
        
        const now = new Date();
        const activeTimeoutMs = 30 * 1000; // 30 seconds timeout
        let isSessionActive = false;
        
        if (sessionRes.rowCount && sessionRes.rowCount > 0) {
          const lastHeartbeat = new Date(sessionRes.rows[0].last_heartbeat);
          if (now.getTime() - lastHeartbeat.getTime() < activeTimeoutMs) {
            isSessionActive = true;
          }
        }
        
        if (isSessionActive) {
          return res.status(403).json({
            error: "Only one active session allowed. You cannot open this assessment in multiple windows/tabs."
          });
        } else {
          // The old session is dead/inactive. Auto-recover by taking over!
          console.log(`[Session Recovery] Auto-recovering session for candidate ${candidate.id}. Swapping old session ${attempt.session_id} to new session ${sessionId}`);
          
          // Update the attempt with the new session ID in the database
          await queryGlobal(
            `UPDATE assessment_attempts SET session_id = $1 WHERE id = $2;`,
            [sessionId, attempt.id]
          );
          
          // Create a new session record for the audit trail
          const browserFingerprint = req.headers['user-agent'] || null;
          const ipAddress = req.ip || req.headers['x-forwarded-for'] || null;
          await queryGlobal(
            `INSERT INTO assessment_sessions (id, candidate_id, assessment_id, attempt_id, status, browser_fingerprint, ip_address)
             VALUES ($1, $2, $3, $4, 'active', $5, $6)
             ON CONFLICT (id) DO NOTHING;`,
            [sessionId, candidate.id, assessmentId, attempt.id, browserFingerprint, ipAddress]
          );
        }
      }

      // Check remaining time
      const elapsedMs = Date.now() - new Date(attempt.started_at).getTime();
      const elapsedSec = Math.floor(elapsedMs / 1000);
      const remainingSeconds = TOTAL_TEST_TIME_SEC - elapsedSec;

      if (remainingSeconds <= 0) {
        // Force submit
        return res.status(403).json({
          error: "Time limit expired. The assessment has been automatically closed.",
          expired: true
        });
      }

      const currentAnswers = typeof attempt.current_answers === "string"
        ? JSON.parse(attempt.current_answers)
        : (attempt.current_answers || {});
      const currentQuestionIndex = attempt.current_question_index || 0;

      return res.json({
        success: true,
        candidateName: candidate.name,
        candidateEmail: candidate.email,
        jobTitle: candidate.role,
        remainingSeconds,
        questions,
        currentAnswers,
        currentQuestionIndex,
      });
    }
  } catch (err: any) {
    console.error("Failed to load assessment portal details:", err);
    res.status(500).json({ error: err.message || "Failed to load assessment details" });
  }
});

